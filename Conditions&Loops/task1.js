const averageGrade = 85;

if (averageGrade < 60) {
    console.log("Unsatisfactory!");
} else if (averageGrade <= 70) {
    console.log("Satisfactory!");
} else if (averageGrade <= 80) {
    console.log("Good!");
} else if (averageGrade <= 90) {
    console.log("Very Good!");
} else {
    console.log("Excellent!")
}