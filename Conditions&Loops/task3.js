// Using a for loog:
const number = 5;

for (let multiplier = 1; multiplier <= 10; multiplier++) {
    const product = number * multiplier;
    console.log(`${number} x ${multiplier} = ${product}`);
}

// Using a while loop:
const number1 = 5;

let multiplier1 = 1;

while (multiplier1 <= 10) {
    const product1 = number1 * multiplier1;
    console.log(`${number1} x ${multiplier1} = ${product1}`);
    multiplier1++
}