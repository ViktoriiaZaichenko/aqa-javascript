// String Concatenation
let person1 = "Mary";
let person2 = "Mark";

let greeting = "Hello, " + person1 + " and " + person2 + "!";

console.log(greeting);

// Template String
let person3 = "Vika";
let person4 = "Sam";

let greeting1 = `Hello, ${person3} and ${person4}!`;

console.log(greeting1); // Output: "Hello, Vika and Sam!"